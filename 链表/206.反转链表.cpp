class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        ListNode *head1= new ListNode(0, head), * head2=new ListNode(0, nullptr);
        ListNode *tmp, *curr;
        while(head1->next!=nullptr){
            curr=head1->next;
            tmp=curr->next;
            
            curr->next=head2->next;
            head2->next=curr;
            head1->next=tmp;
        }

        tmp=head2->next;
        delete head1, head2;
        return tmp;
    }
};
